import { useState, useEffect } from "react";
import { Container, Row, Col } from "react-bootstrap";
import headerImg from "../assets/img/header-img.svg";
import { ArrowRightCircle } from 'react-bootstrap-icons';
import 'animate.css';
import TrackVisibility from 'react-on-screen';
import { Contact } from "./Contact";



export const Banner = () => {
  const [loopNum, setLoopNum] = useState(0);
  const [isDeleting, setIsDeleting] = useState(false);
  const [text, setText] = useState('');
  const [delta, setDelta] = useState(300 - Math.random() * 100);
  const [index, setIndex] = useState(1);
  const toRotate = ["Data Analyst" ,"Frontend Developer"];
  const period = 2000;

  useEffect(() => {
    let ticker = setInterval(() => {
      tick();
    }, delta);

    return () => { clearInterval(ticker) };
  }, [text])

  const tick = () => {
    let i = loopNum % toRotate.length;
    let fullText = toRotate[i];
    let updatedText = isDeleting ? fullText.substring(0, text.length - 1) : fullText.substring(0, text.length + 1);

    setText(updatedText);

    if (isDeleting) {
      setDelta(prevDelta => prevDelta / 2);
    }

    if (!isDeleting && updatedText === fullText) {
      setIsDeleting(true);
      setIndex(prevIndex => prevIndex - 1);
      setDelta(period);
    } else if (isDeleting && updatedText === '') {
      setIsDeleting(false);
      setLoopNum(loopNum + 1);
      setIndex(1);
      setDelta(500);
    } else {
      setIndex(prevIndex => prevIndex + 1);
    }
  }

  return (
    <section className="banner" id="home">
      <Container>
        <Row className="aligh-items-center">
          <Col xs={12} md={6} xl={7}>
            <TrackVisibility>
              {({ isVisible }) =>
              <div className={isVisible ? "animate__animated animate__fadeIn" : ""}>
                <span className="tagline">Welcome to my Portfolio</span>
                <h1>{`Hi! I'm Subramanyam`} <span className="txt-rotate" dataPeriod="1000" data-rotate='[ "Data Analyst","Frontend Developer" ]'><span className="wrap">{text}</span></span></h1>
            <p>    I’m a passionate Data Analyst skilled in transforming raw data into actionable insights.

With 1.5 months of experience as a Customer Service Associate, I’ve honed my ability to understand user needs, communicate insights clearly, and solve problems efficiently—skills that enhance my data-driven approach. Whether analyzing hotel bookings, shipment logistics, or customer behavior, I love turning numbers into meaningful stories that drive decisions.

Currently, I’m advancing my skills through hands-on projects and certifications like Cisco’s Data Analytics Essentials, while actively seeking opportunities to contribute my analytical mindset to data-driven teams.

 exploring new ways to learn and grow. Let’s connect and unlock the power of data together!</p>  
   
 <button onClick={() => {
  const section = document.getElementById('connect');
  section?.scrollIntoView({ behavior: 'smooth' });
}}>
  Let’s Connect <ArrowRightCircle size={25} />
</button>
              </div>}
            </TrackVisibility>
          </Col>
          <Col xs={12} md={6} xl={5}>
            <TrackVisibility>
              {({ isVisible }) =>
                <div className={isVisible ? "animate__animated animate__zoomIn" : ""}>
                  <img src={headerImg} alt="Header Img"/>
                </div>}
            </TrackVisibility>
          </Col>
        </Row>
      </Container>
    </section>
  )
}
